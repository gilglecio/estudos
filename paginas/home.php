		<h2>Anonymous sequestra PCs da Anatel e exige ação contra internet limitada</h2>

		<img width="300px" title="Imagem do Anonimous" alt="Imagem do Anonimous" src="/img/img1.jpg">

		<p>Uma célula brasileira da Anonymous acabou de divulgar que teve sucesso em sequestrar, por meio de ransomwares, computadores da Anatel (Agência Nacional de Telecomunicações). Segundo o grupo, em manifesto, "o objetivo é cobrar da agência uma posição firme, imutável e permanente sobre o fim da franquia de dados na internet fixa".</p>
